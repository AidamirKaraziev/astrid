def load_admin1_english(path: Path) -> dict[str, str]:
    """Парсит admin1CodesASCII.txt → {код: английское имя}."""
    mapping: dict[str, str] = {}
    if not path.exists():
        return mapping
    for line in path.read_text(encoding="utf-8").splitlines():
        if not line or line.startswith("#"):
            continue
        code, _, name = line.partition("\t")
        if code.startswith("RU."):
            mapping[code[3:]] = name.strip()
    return mapping


def admin1_name_ru(admin1_code: str | None, fallback_en: str | None = None) -> str | None:
    if not admin1_code:
        return fallback_en
    return RU_ADMIN1_RU.get(admin1_code, fallback_en)
